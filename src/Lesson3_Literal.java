public class Lesson3_Literal {
    public static void main(String[] args) {
        //--Integral Literal
        byte age = 44;
        int num = 55;
        short sot = 43;
        long lng = 44444444444444L;
        //------------------
        //--Character Literal
        char ch = 'c';
        //------------------
        //--Float Literal
        float f1 = 5.6f;
        double dble = 3.55d;
        //------------------
        String str = "Devraj";
        System.out.println("The age is : " + age);
        System.out.println("The num is : " + num);
        System.out.println("The sot is : " + sot);
        System.out.println("The lng is : " + lng);
        System.out.println("The ch is : " + ch);
        System.out.println("The f1 is : " + f1);
        System.out.println("The dble is : " + dble);
        System.out.println("The age is : " + str);
    }
}

/*
* Primitive Data Type
* 1. Integral - byte, short, int, long
* 2. Float - float, double
* 3. Char - ('')
* 4. Boolean - True/False
* literal is a Value which is store in Variable
* */
